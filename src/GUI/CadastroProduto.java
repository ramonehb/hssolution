    private boolean ValidaCadastro(){
        String mensagem = "";
        int erro = 0;
        
        if (jTextNomeProd.getText().equals("")){
            mensagem += "Digite o nome!\n";
            erro++;
        }
        
        if (jTextQuantidade.getText().equals("")){
            mensagem += "Digite a quantidade!\n";
            erro++;
        }
        
        if (jTextValorVenda.getText().equals("")){
            mensagem += "Digite o valor de venda!\n";
            erro++;
        }
        
        if (jTextValorPago.getText().equals("")){
            mensagem += "Digite o valor de compra!\n";
            erro++;
        }
        if (erro  >  0){
           JOptionPane.showMessageDialog(null, mensagem, "Atenção", JOptionPane.CANCEL_OPTION);
       }       
       
       return erro == 0;

    }
    
    public void listaTipoUsuario(){
        try {
            ProdutoDAL produtoDAL = new ProdutoDAL();
            ResultSet res = produtoDAL.listaTipoProduto();
            while (res.next()){
                idTipoProduto.addElement(res.getInt(1));
                jComboTipo.addItem(res.getString(2));
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null,"Contate o administrador\nErro: "+e.getMessage(),"Atenção", JOptionPane.INFORMATION_MESSAGE);
        }
    }    
    
