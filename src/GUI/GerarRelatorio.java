    private void jButtonGerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGerarActionPerformed
        try {
            SimpleDateFormat dt = new SimpleDateFormat("dd/MM/yyyy");
            if ("Por período".equals(jComboTipo.getSelectedItem().toString()) && "".equals(jFormattedTextDataInicial.getText())){
                JOptionPane.showMessageDialog(null,"Digite a data inicial.","Atenção", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            if ("Por período".equals(jComboTipo.getSelectedItem().toString()) && "".equals(jFormattedTextDataFinal.getText())){
                JOptionPane.showMessageDialog(null,"Digite a data final.","Atenção", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            
            if ("Por período".equals(jComboTipo.getSelectedItem().toString())){
                Date dtInicial = dt.parse(jFormattedTextDataInicial.getText());
                Date dtFinal = dt.parse(jFormattedTextDataFinal.getText());
                gerarRelatorioPorPeriodo(dtInicial,dtFinal);
            } else {
                gerarRelatorioCompleto();
            }
            
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(null,"Contate o administrador\nErro: "+e.getMessage(),"Atenção", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButtonGerarActionPerformed

