    private void gerarRelatorioPorPeriodo(Date inicio, Date fim){
        DefaultTableModel modelo = (DefaultTableModel) jTableRelatorio.getModel();
        modelo.setNumRows(0);
        
        jTableRelatorio.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTableRelatorio.getColumnModel().getColumn(1).setPreferredWidth(70);
        jTableRelatorio.getColumnModel().getColumn(2).setPreferredWidth(150);
        jTableRelatorio.getColumnModel().getColumn(3).setPreferredWidth(50);
        
        try {
            RelatorioDAL relatorioDAL = new RelatorioDAL();
        
        for (Relatorio r : relatorioDAL.listarRelatorio(inicio, fim)){
            modelo.addRow(new Object[]{
                r.IdPedido,
                r.NomeCliente,
                r.FormaPagamento,
                r.PedidoStatus,
                r.DataVenda,
                r.NomeProduto,
                r.Quantidade,
                r.Total
            });
        }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null,"Contate o administrador\nErro: "+e.getMessage(),"Atenção", JOptionPane.INFORMATION_MESSAGE);
        }
    }
